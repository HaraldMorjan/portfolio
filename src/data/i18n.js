export function getUi(lang = DEFAULT_LANG) {
  return ui[lang] || ui[DEFAULT_LANG];
}

// Locale-aware path helpers. English is served at the root, Spanish under /es/.
export function localizedPath(lang, path = "/") {
  const clean = path.startsWith("/") ? path : `/${path}`;
  if (lang === DEFAULT_LANG) return clean;
  return clean === "/" ? `/${lang}/` : `/${lang}${clean}`;
}
